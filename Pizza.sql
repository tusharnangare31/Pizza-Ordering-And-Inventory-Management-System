CREATE DATABASE Pizza ;

USE Pizza;

CREATE TABLE pizza(
            id INT PRIMARY KEY,
            pizza_name VARCHAR(20),
            price INT,
            quantity INT
            );

CREATE TABLE orders (
    id INT AUTO_INCREMENT PRIMARY KEY,
    customer_name VARCHAR(20),
    Margherita_Pizza INT,
    BBQ_Pizza INT,
    SeaFood_Pizza INT,
    total_price INT
);

